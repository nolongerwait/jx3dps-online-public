const 获取阈值 = (原始帧数 = 24) => {
  let res = {}
  let 加速等级 = 0 
  while(!res[5]) {
    const 当前帧数 = 获取实际帧数(原始帧数, 加速等级)
    if ((原始帧数 - 当前帧数) > 0) {
      const 加速档位 = 原始帧数 - 当前帧数
      if (!res[加速档位]) {
        res[加速档位] = 加速等级
      }
    }
    加速等级 = 加速等级 + 1
  }

  console.log(`${原始帧数} 帧模型阈值:`, res)
  return res
}

const 获取实际帧数 = (原始帧数, 加速等级) => {
  return Math.floor((1024 * 原始帧数) / (Math.floor((1024 * 加速等级) / 210078) + 1024))
}

