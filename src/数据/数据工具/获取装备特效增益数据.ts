import { 装备增益数据类型 } from '@/@types/装备'

const 获取装备特效增益数据 = (
  增益名称: string,
  特效等级: number,
  装备增益数据: 装备增益数据类型,
  覆盖率 = 1
) => {
  if (装备增益数据?.[增益名称]) {
    return 装备增益数据?.[增益名称]?.map((增益) => {
      const 叠加层数 = 增益?.可叠加 ? 特效等级 || 1 : 1
      const 增益基础值 = 增益?.值[特效等级 - 1] || 增益?.值[0] || 0
      return {
        属性: 增益?.属性,
        值: 增益基础值 * 覆盖率 * 叠加层数,
      }
    })
  } else {
    return []
  }
}

export default 获取装备特效增益数据
