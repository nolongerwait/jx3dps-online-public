import { 属性类型 } from '@/@types/属性'
import { 装备部位枚举 } from '@/@types/枚举'
import { 附魔数据类型 } from '@/@types/附魔'

const 附魔数组 = [359, 308, 268, 154, 134]

// 按顺序位置索引
const 附魔分数索引 = [2877, 2466, 2282, 1231, 1139]

export const 获取主属性附魔数据 = (主属性?): 附魔数据类型[] => {
  return 附魔数组.map((item, index) => {
    return {
      附魔名称: `${主属性}+${item}`,
      附魔支持部位: [装备部位枚举.护腕, 装备部位枚举.下装, 装备部位枚举.戒指, 装备部位枚举.暗器],
      增益集合: [{ 属性: 属性类型?.[主属性], 值: item }],
      附魔装分: 附魔分数索引[index],
    }
  })
}
