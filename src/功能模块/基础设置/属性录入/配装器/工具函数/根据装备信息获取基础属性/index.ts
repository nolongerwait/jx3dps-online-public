/**
 * @name 精炼加成系数算法
 */
export const 精炼加成系数算法 = (基础, 等级) => {
  return 基础 + Math.round(基础 * 精炼加成系数[等级])
}

export const 判断装备增益 = (装备增益原始数据: 装备增益原始数据类型): 装备增益类型 => {
  const 装备增益: Partial<装备增益类型> = {}
  Object.keys(装备增益原始数据)?.forEach((该装备特效) => {
    switch (该装备特效) {
      case 装备特效枚举.水特效武器:
      case 装备特效枚举.风特效腰坠:
      case 装备特效枚举.大橙武特效:
      case 装备特效枚举.小橙武特效:
      case 装备特效枚举.龙门飞剑武器:
      case 装备特效枚举.试炼鞋子破防:
      case 装备特效枚举.试炼适应之力:
      case 装备特效枚举.试炼项链破防:
      case 装备特效枚举.试炼腰坠破防:
      case 装备特效枚举.试炼暗器特效:
      case 装备特效枚举.试炼项链会心:
      case 装备特效枚举.试炼鞋子会心:
      case 装备特效枚举.试炼腰坠会效:
      case 装备特效枚举.特效_2869:
      case 装备特效枚举.特效_2870:
      case 装备特效枚举.特效_2871:
      case 装备特效枚举.特效_2872:
      case 装备特效枚举.特效_2873:
      case 装备特效枚举.特效_2880:
        装备增益[该装备特效] = 装备增益原始数据?.[该装备特效]?.等级 as any
        break
      case 装备特效枚举.特效_2874:
      case 装备特效枚举.特效_2875:
      case 装备特效枚举.特效_2876:
        装备增益[该装备特效] = 装备增益原始数据?.[该装备特效]?.数量 as any
        break
      // case 装备特效枚举.门派套装:
      //   装备增益.套装会心会效 = 装备增益原始数据?.[该装备特效]?.数量 >= 2 ? 1 : 0
      //   装备增益.套装技能 = 装备增益原始数据?.[该装备特效]?.数量 >= 4 ? 1 : 0
      //   break
      case 装备特效枚举.新门派套装:
        装备增益.常驻套装会心会效 = 装备增益原始数据?.[该装备特效]?.数量 >= 2 ? 1 : 0
        装备增益.套装技能 = 装备增益原始数据?.[该装备特效]?.数量 >= 4 ? 1 : 0
        break
      case 装备特效枚举.冬至套装:
        装备增益.冬至套装 = 装备增益原始数据?.[该装备特效]?.数量 >= 2 ? 1 : 0
        break
      case 装备特效枚举.切糕_普通:
        装备增益.切糕会心 = 装备增益原始数据?.[该装备特效]?.数量 >= 2 ? 1 : 0
        装备增益.切糕无双 = 装备增益原始数据?.[该装备特效]?.数量 >= 4 ? 1 : 0
        break
      case 装备特效枚举.切糕_英雄:
        装备增益.切糕会心_英雄 = 装备增益原始数据?.[该装备特效]?.数量 >= 2 ? 1 : 0
        装备增益.切糕无双_英雄 = 装备增益原始数据?.[该装备特效]?.数量 >= 4 ? 1 : 0
        break
    }
  })
  return {
    ...装备增益,
  }
}

export const 判断五彩石郭氏增益 = (五彩石郭氏增益: 属性加成[], 最终装备增益: 装备增益类型) => {
  const 计算后增益: any = {
    ...最终装备增益,
    郭氏力道: 0,
    郭氏根骨: 0,
    郭氏身法: 0,
    郭氏元气: 0,
  }
  五彩石郭氏增益.forEach((增益) => {
    通用增益计算(增益, 计算后增益)
  })
  return 计算后增益
}

