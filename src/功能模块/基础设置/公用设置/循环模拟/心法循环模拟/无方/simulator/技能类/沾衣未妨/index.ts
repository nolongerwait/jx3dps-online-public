import 循环模拟技能基础数据 from '../../../constant/skill'
import { 待生效事件 } from '../../type'
import 有CD技能通用类 from '../../通用类/有CD技能通用类'

class 沾衣未妨 extends 有CD技能通用类 {
  // scripts/skill/北天药宗/北天药宗_沾衣未妨.lua
  static 技能数据 = 循环模拟技能基础数据?.find((item) => item.技能名称 === '沾衣未妨')
  static 作用次数 = 5
  static 作用间隔 = 12
  static 首次间隔 = 5
  static 香繁饮露触发 = false

  constructor(模拟循环) {
    super(模拟循环)
    this.初始化技能运行数据()
  }

  命中(_, 是否触发蛰伏) {
    this.保存释放记录('沾衣未妨')
    if (this.模拟循环.当前自身buff列表?.['香繁饮露']?.当前层数) {
      沾衣未妨.香繁饮露触发 = true
      沾衣未妨.作用次数 = 3
      if (this.模拟循环?.Buff和Dot数据?.['沾衣未妨']) {
        this.模拟循环.Buff和Dot数据['沾衣未妨'].最大持续时间 =
          沾衣未妨.作用间隔 * 3 + 沾衣未妨.首次间隔
      }
      this.减少调息时间(Math.floor((沾衣未妨?.技能数据?.技能CD || 0) * 0.7))
    } else {
      沾衣未妨.香繁饮露触发 = false
      沾衣未妨.作用次数 = 5
      if (this.模拟循环?.Buff和Dot数据?.['沾衣未妨']) {
        this.模拟循环.Buff和Dot数据['沾衣未妨'].最大持续时间 =
          沾衣未妨.作用间隔 * 5 + 沾衣未妨.首次间隔
      }
    }
    if (是否触发蛰伏 && this.模拟循环.当前自身buff列表['苍棘缚地蛰伏']?.当前层数) {
      this.模拟循环.技能类实例集合?.['苍棘缚地']?.立即生长()
    }
    this.沾衣未妨持续伤害(沾衣未妨.香繁饮露触发)
    this.模拟循环.添加buff({ 名称: '沾衣未妨', 对象: '目标' })
    this.模拟循环.卸除buff({ 名称: '香繁饮露', 对象: '自身' })
  }

  沾衣未妨持续伤害(香繁饮露触发) {
    const 待生效事件队列: 待生效事件[] = []
    const 作用次数 = 沾衣未妨.作用次数
    for (let i = 0; i < 作用次数; i++) {
      待生效事件队列.push({
        事件名称: '沾衣未妨',
        事件时间: this.模拟循环.当前时间 + 沾衣未妨.首次间隔 + 沾衣未妨.作用间隔 * (i + 1),
        事件备注: {
          技能名称: '沾衣未妨',
          香繁饮露触发: !!香繁饮露触发,
        },
      })
    }
    this.模拟循环.添加待生效事件队列(待生效事件队列)
  }

  沾衣未妨结算(香繁饮露触发) {
    this.改变温寒(-1, '沾衣未妨')
    this.触发伤害行为('沾衣未妨')
    if (香繁饮露触发) {
      this.改变温寒(-1, '沾衣未妨_香繁饮露')
    }
  }

  六微校验() {
    return this.模拟循环.校验奇穴是否存在('六微') && this?.模拟循环?.角色状态信息?.温寒 >= 5
  }

  保存释放记录(名称) {
    this.本次释放记录 = {
      实际伤害技能: 名称,
      重要buff列表: this.获取当前重要buff列表(['相使']),
    }
  }
}

export default 沾衣未妨

export const 沾衣未妨类型 = typeof 沾衣未妨
