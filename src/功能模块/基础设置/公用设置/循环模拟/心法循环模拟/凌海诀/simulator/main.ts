  // 增加技能GCD
  增加技能GCD(当前技能: 循环基础技能数据类型) {
    // GCD处理
    if (当前技能?.技能GCD组) {
      let 实际添加GCD = this.获取物化加速帧数(当前技能?.技能释放后添加GCD, this.加速值)
      if (当前技能?.最小GCD) {
        实际添加GCD = Math.max(实际添加GCD, 当前技能?.最小GCD)
      }
      let 待更新GCD组: string = 当前技能.技能GCD组 as string
      if (当前技能.技能GCD组 === '自身') {
        待更新GCD组 = 当前技能?.技能名称
      }
      if (当前技能?.技能释放后添加GCD) {
        if (待更新GCD组) {
          this.GCD组[待更新GCD组] = (this.GCD组[待更新GCD组] || 0) + 实际添加GCD
        }
      }
    }
  }

