  // 模拟函数，一个技能的释放生命周期
  模拟() {
    for (let i = 0; i < this.测试循环.length; i++) {
      if (i === 0) {
        this.技能类实例集合.飘遥伞击.初始化伞击轴()
      }
      let 实际技能名称 = this.测试循环[i]
      let 额外信息 = null
      if (this.测试循环[i]?.includes(技能额外信息分隔符)) {
        const list = this.测试循环[i]?.split(技能额外信息分隔符)
        实际技能名称 = list[0]
        try {
          额外信息 = JSON.parse(list[1])
        } catch (e) {
          console.error('e', e)
        }
      }
      this.本轮模拟开始前()
      const 当前技能 = this?.技能基础数据?.find((item) => item?.技能名称 === 实际技能名称)
      const 跳过运行 = 当前技能?.技能名称 === '翼绝云天' && this.自动鸟啄
      if (当前技能) {
        const 技能实例 = this.技能类实例集合[当前技能?.技能名称]
        if (技能实例) {
          if (!跳过运行) {
            技能实例?.释放前初始化?.(额外信息)
            技能实例?.释放前?.()
            // 获取预估技能释放时间，处理预估时间前的所有待生效事件，推进时间轴
            const { 技能计划释放时间, 等待CD, 技能预估释放时间 } = this.技能释放前(
              当前技能,
              技能实例,
              i,
            )
            const 读条时间 = 技能实例?.获取读条时间?.(i) || 0
            const 是否为读条技能 = !!读条时间
            if (读条时间) {
              技能实例?.读条?.(技能预估释放时间)
            }
            this.待生效事件结算(技能预估释放时间)
            this.技能GCD和CD处理(等待CD, 技能预估释放时间, 当前技能, 技能实例)
            // this.清空已经消失的buff()
            const 开始读条时间 = 是否为读条技能 ? 技能预估释放时间 : undefined
            const 结束读条时间 = 是否为读条技能 ? 技能预估释放时间 + 读条时间 : 0
            this.技能成功开始释放(当前技能, 技能实例)
            const 释放校验结果 = this.技能释放校验(技能实例, 当前技能)
            const 是否为最后一个技能 = i === this.测试循环.length - 1
            const 开始释放时间 = this.当前时间
            this.清空已经消失的buff()
            if (释放校验结果?.可以释放) {
              技能实例.命中?.(是否为最后一个技能)
              技能实例.造成伤害?.(i)
              // 推进到读条结束时间
              if (结束读条时间 && 结束读条时间 > this.当前时间) {
                this.待生效事件结算(结束读条时间)
              }
              技能实例.释放后?.(i)
            } else {
              this.技能释放异常(释放校验结果, 当前技能, i)
              break
            }

            this.技能释放后(
              当前技能,
              技能计划释放时间,
              开始读条时间,
              开始释放时间,
              是否为读条技能,
              技能实例,
            )
          } else {
            this.技能释放后(当前技能, 0, 0, 0, false, 技能实例)
          }
        }
      }
      this.本轮模拟结束后()
    }
  }

