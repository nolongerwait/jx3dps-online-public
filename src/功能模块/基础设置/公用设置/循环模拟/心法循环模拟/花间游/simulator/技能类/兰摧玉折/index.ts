import { 获取实际帧数 } from '@/工具函数/data'
import 循环模拟技能基础数据 from '../../../constant/skill'
import { 待生效事件 } from '../../type'
import 有CD技能通用类 from '../../通用类/有CD技能通用类'
import { 判断秘籍减读条 } from '../../../../通用/通用函数'

class 兰摧玉折 extends 有CD技能通用类 {
  // scripts/skill/万花/万花_百花拂穴手_兰摧玉折.lua
  static 技能数据 = 循环模拟技能基础数据?.find((item) => item.技能名称 === '兰摧玉折')
  static 总作用间隔 = 28
  static 读条时间 = 28
  static 本次释放是否读条 = true
  static 本次释放墨意变化 = 0
  static 获得墨意 = 5

  constructor(模拟循环) {
    super(模拟循环)
    this.初始化技能运行数据()
    兰摧玉折.技能数据 = 模拟循环?.技能基础数据?.find((item) => item.技能名称 === '兰摧玉折')
  }

  获取读条时间(技能索引) {
    if (this.水月校验() || this.钟灵校验() || 技能索引 === 0) {
      兰摧玉折.本次释放是否读条 = false
      return 0
    }

    const 读条时间 = this.获取兰摧玉折读条时间()
    兰摧玉折.本次释放是否读条 = true
    return 读条时间
  }

  获取兰摧玉折读条时间() {
    const 秘籍减读条时间 = 判断秘籍减读条('兰摧玉折', this.模拟循环.秘籍)
    const 读条持续时间 = 获取实际帧数(兰摧玉折.总作用间隔 - 秘籍减读条时间, this.模拟循环.加速值)
    兰摧玉折.读条时间 = 读条持续时间
    return 读条持续时间
  }

  读条(读条开始时间) {
    this?.保存释放记录('兰摧玉折')
    // 开始读条
    this.读条兰摧玉折(读条开始时间)
  }

  读条兰摧玉折(读条开始时间) {
    const 待生效事件队列: 待生效事件[] = []
    待生效事件队列.push({
      事件名称: '技能读条',
      事件时间: 读条开始时间 + 兰摧玉折.读条时间,
      事件备注: {
        技能名称: '兰摧玉折',
      },
    })
    this.模拟循环.添加待生效事件队列(待生效事件队列)
  }

  读条命中() {
    // 正读条引在读条结束后才进入CD
    if (兰摧玉折.技能数据) {
      this.模拟循环?.技能释放后更新运行数据?.(兰摧玉折.技能数据, this)
    }
    this.兰摧玉折命中()
  }

  命中(释放索引) {
    if (!兰摧玉折.本次释放是否读条 || 释放索引 === 0) {
      this.兰摧玉折命中()
    }
  }

  兰摧玉折命中() {
    this?.模拟循环?.技能类实例集合?.DOT_兰摧玉折?.获得和刷新兰摧玉折()

    this.折花伤害触发判定('兰摧')
    this.焚玉伤害触发判定()

    兰摧玉折.本次释放墨意变化 = 0
    if (this.钟灵校验()) {
      兰摧玉折.本次释放墨意变化 = -20
    } else if (this.水月校验()) {
      兰摧玉折.本次释放墨意变化 = -20
    }
    this.模拟循环?.卸除buff({ 名称: '水月瞬发', 对象: '自身' })
    this.模拟循环?.卸除buff({ 名称: '钟灵瞬发', 对象: '自身' })
    if (this.模拟循环?.大橙武模拟) {
      // 25%概率造成一次，这里取期望0.25次
      this.触发伤害行为?.('兰摧玉折·神兵', 0.25)
    }
    this.墨意变化(兰摧玉折.获得墨意 + 兰摧玉折.本次释放墨意变化)
    this?.涓流函数()
  }

  保存释放记录(名称) {
    const 保存显示段数 = this.模拟循环.显示涓流层数
      ? this.模拟循环.当前自身buff列表['涓流']?.当前层数 || 0
      : undefined

    this.本次释放记录 = {
      伤害段数: 保存显示段数,
      实际伤害技能: 名称,
      重要buff列表: this.获取当前重要buff列表(['钟灵', '倚天', '涓流', '乱洒青荷', '布散畅和']),
    }
  }
}

export default 兰摧玉折

export const 兰摧玉折类型 = typeof 兰摧玉折
